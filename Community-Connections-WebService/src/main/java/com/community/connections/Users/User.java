package com.community.connections.Users;

public class User {
    public String username;
    public String password;
    public String email;
    public String firstName;
    public String lastName;
    public String phoneNum;
    public String authQuestion;
    public String authAnswer;

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getPhoneNum() {
        return phoneNum;
    }

    public void setPhoneNum(String phoneNum) {
        this.phoneNum = phoneNum;
    }

    public String getAuthQuestion() {
        return authQuestion;
    }

    public void setAuthQuestion(String authQuestion) {
        this.authQuestion = authQuestion;
    }

    public String getAuthAnswer() {
        return authAnswer;
    }

    public void setAuthAnswer(String authAnswer) {
        this.authAnswer = authAnswer;
    }
}
